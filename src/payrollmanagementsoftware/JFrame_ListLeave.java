    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int seletedRows = jTable1.getSelectedRow();
        try {
            int id = Integer.parseInt(tableModel.getValueAt(seletedRows, 0).toString());
            System.out.println("ID : " + id);
            String leave_id = String.valueOf(id);

            JFrame_Leave frame = new JFrame_Leave();

            String sql = "SELECT * from Leave INNER JOIN Employee ON Leave.emp_id = Employee.emp_id AND Leave.leave_id = '" + id + "'";

            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String add1 = rs.getString("emp_name");
                frame.emp_name.setText(add1);

                String add2 = rs.getString("emp_shift");
                frame.emp_shift.setSelectedItem(add2);

                String add3 = rs.getString("leave_type");
                frame.leave_type.setSelectedItem(add3);

                String add6 = rs.getString("start_date");
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                Date start = sdf.parse(add6);
                frame.leave_start.setDate(start);

                String add7 = rs.getString("end_date");
                Date end = sdf.parse(add7);
                frame.leave_end.setDate(end);

                String add8 = rs.getString("days");
                frame.emp_shift.setSelectedItem(add8);

                String add9 = rs.getString("reason");
                frame.leave_reason.setText(add9);
                
                byte[] image = rs.getBytes("emp_img");
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(frame.img.getWidth(), frame.img.getHeight(), Image.SCALE_SMOOTH));
                frame.img.setIcon(imageIcon);

                frame.leaveID.setText(leave_id);  
                frame.emp_code1.setEditable(false);
                frame.jButton1.setEnabled(false);
                frame.save.setEnabled(false);
                frame.update.setEnabled(true);
                frame.setVisible(true);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

