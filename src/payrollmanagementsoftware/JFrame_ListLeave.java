    private void refreshTable() {
        try {
            String sql = "select * from Leave";
            pst = conn.prepareStatement(sql);

            rs = pst.executeQuery();
            ResultSetMetaData data = rs.getMetaData();
            int q = data.getColumnCount();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector columnData = new Vector();
                for (int i = 1; i <= q; i++) {
                    columnData.add(rs.getString("leave_id"));
                    columnData.add(rs.getString("emp_id"));
                    columnData.add(rs.getString("leave_type"));
                    columnData.add(rs.getString("start_date"));
                    columnData.add(rs.getString("end_date"));
                    columnData.add(rs.getString("days"));
                    columnData.add(rs.getString("reason"));
                }
                model.addRow(columnData);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

